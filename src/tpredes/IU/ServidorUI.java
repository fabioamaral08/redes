    public void atualiza(){
        DefaultTableModel model = (DefaultTableModel) this.tb_servidor.getModel();
        Object[] ob = new Object[3];

        model.setNumRows(0);
        for (JogadorS j : server.getPlayers()) {
            InetAddress ip = j.getIP();
            ob[0] = ip.getHostAddress();
            ob[1] = j.getPorta();
            ob[2] = j.getStatus();

            model.addRow(ob);

        }
    }
    
